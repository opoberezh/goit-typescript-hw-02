let some:unknown;
some = 'Text';
let str: string;
str = some as string;

export {};

// використання оператора as для приведення типу,  є допустимим і ефективним способом. Коли ви пишете str = some as string;, ви явно кажете TypeScript, що ви впевнені, що some має тип string. Це дозволяє уникнути будь-яких помилок перевірки типу на етапі компіляції, і вказує компілятору припустити, що some має тип string.